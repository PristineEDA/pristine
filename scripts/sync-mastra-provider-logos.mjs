import { mkdir, readdir, readFile, writeFile } from 'node:fs/promises'
import path from 'node:path'

const workspaceRoot = process.cwd()
const catalogPath = path.join(
  workspaceRoot,
  'src',
  'app',
  'components',
  'assistant',
  'mastraStudioModelCatalog.generated.ts',
)
const logoOutputDir = path.join(workspaceRoot, 'public', 'model-provider-logos')
const manifestPath = path.join(
  workspaceRoot,
  'src',
  'app',
  'components',
  'assistant-ui',
  'model-provider-logo-manifest.generated.ts',
)
const logoBaseUrl = 'https://models.dev/logos'

const logoIdOverrides = new Map([
  ['fireworks-ai', 'fireworks'],
  ['fireworks_ai', 'fireworks'],
])

function normalizeProviderLogoId(providerId) {
  const baseProviderId = providerId.includes('.') ? providerId.split('.')[0] : providerId
  const mappedProviderId = logoIdOverrides.get(baseProviderId) ?? baseProviderId
  return mappedProviderId.replace(/\//g, '-').toLowerCase()
}

function parseCatalog(source) {
  const marker = 'const catalogJson = '
  const start = source.indexOf(marker)

  if (start === -1) {
    throw new Error(`Could not find ${marker} in generated catalog`)
  }

  const end = source.indexOf(';\n\nexport const mastraStudioModelCatalog', start)

  if (end === -1) {
    throw new Error('Could not find the end of catalogJson in generated catalog')
  }

  const stringLiteral = source.slice(start + marker.length, end).trim()
  return JSON.parse(JSON.parse(stringLiteral))
}

function looksLikeSvg(content) {
  return /<svg[\s>]/i.test(content) || /<\?xml/i.test(content)
}

async function getExistingLogoIds() {
  try {
    const files = await readdir(logoOutputDir)
    return new Set(
      files
        .filter((file) => file.endsWith('.svg'))
        .map((file) => file.slice(0, -'.svg'.length)),
    )
  } catch {
    return new Set()
  }
}

async function downloadLogo(logoId) {
  const url = `${logoBaseUrl}/${logoId}.svg`
  const response = await fetch(url, {
    signal: AbortSignal.timeout(10_000),
  })

  if (!response.ok) {
    return { ok: false, reason: `${response.status} ${response.statusText}` }
  }

  const content = await response.text()

  if (!looksLikeSvg(content)) {
    return { ok: false, reason: 'response was not SVG' }
  }

  await writeFile(path.join(logoOutputDir, `${logoId}.svg`), content)
  return { ok: true }
}

function serializeManifest(entries, logoIds) {
  return `// This file is generated by scripts/sync-mastra-provider-logos.mjs.
// Source: ${logoBaseUrl}/{logoId}.svg

export type ModelProviderLogoManifestEntry = {
  readonly providerId: string;
  readonly logoId: string;
  readonly path: string;
  readonly hasLogo: boolean;
};

export const modelProviderLogoManifest = ${JSON.stringify(entries, null, 2)} as const satisfies readonly ModelProviderLogoManifestEntry[];

export const availableModelProviderLogoIds = ${JSON.stringify([...logoIds].sort(), null, 2)} as const;
`
}

const catalogSource = await readFile(catalogPath, 'utf8')
const providers = parseCatalog(catalogSource)
const providerEntries = providers.map((provider) => ({
  providerId: provider.id,
  logoId: normalizeProviderLogoId(provider.id),
}))
const uniqueLogoIds = [...new Set(providerEntries.map((entry) => entry.logoId))].sort()

await mkdir(logoOutputDir, { recursive: true })

const availableLogoIds = await getExistingLogoIds()
let downloadedCount = 0
let skippedCount = 0
let failedCount = 0

for (const logoId of uniqueLogoIds) {
  if (availableLogoIds.has(logoId)) {
    skippedCount += 1
    continue
  }

  try {
    const result = await downloadLogo(logoId)

    if (result.ok) {
      availableLogoIds.add(logoId)
      downloadedCount += 1
    } else {
      failedCount += 1
      console.warn(`Could not download ${logoId}.svg: ${result.reason}`)
    }
  } catch (error) {
    failedCount += 1
    const message = error instanceof Error ? error.message : String(error)
    console.warn(`Could not download ${logoId}.svg: ${message}`)
  }
}

const manifestEntries = providerEntries.map((entry) => ({
  providerId: entry.providerId,
  logoId: entry.logoId,
  path: `/model-provider-logos/${entry.logoId}.svg`,
  hasLogo: availableLogoIds.has(entry.logoId),
}))

await writeFile(manifestPath, serializeManifest(manifestEntries, availableLogoIds))

console.log(
  `Synced Mastra provider logos: ${downloadedCount} downloaded, ${skippedCount} already local, ${failedCount} unavailable, ${availableLogoIds.size} local logo files.`,
)
